function pickRandom<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)];
}

function shuffleArray<T>(arr: T[]): T[] {
  const a = [...arr];
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

function generateExtraFields(): Partial<Row> {
  return {
    periodicity: pickRandom([...PERIODICITIES]),
    complexity: pickRandom([...COMPLEXITIES]),
    artifact: pickRandom(mockArtifacts),
    basis: pickRandom(mockBases),
    artifactUsage: pickRandom(mockArtifactUsages),
    purpose: pickRandom(mockPurposes),
    efficiency: pickRandom([...EFFICIENCIES]),
    transferTo: pickRandom([...TRANSFER_TO_OPTIONS]),
    controlPoint: pickRandom([...CONTROL_POINTS]),
    nextAction: pickRandom([...NEXT_ACTIONS]),
  };
}

export function generateFunctionDetails(): FunctionDetails {
  const rows: Row[] = [];
  const MAX_PER_CATEGORY = 5;

  for (const cat of CATEGORIES) {
    const s1Texts = shuffleArray(step1Texts[cat]);
    const s2Texts = shuffleArray(step2Texts[cat]);

    const s1Count = Math.min(s1Texts.length, 2 + Math.floor(Math.random() * 2));
    const s2Count = Math.min(s2Texts.length, MAX_PER_CATEGORY - s1Count);

    for (let i = 0; i < s1Count; i++) {
      rows.push({
        id: createRowId(1),
        step: 1,
        category: cat,
        detailText: s1Texts[i],
        who: pickRandom(whos),
        actionLabel: pickRandom([...ACTIONS]),
        ...generateExtraFields(),
      });
    }

    for (let i = 0; i < s2Count; i++) {
      rows.push({
        id: createRowId(2),
        step: 2,
        category: cat,
        detailText: s2Texts[i],
        who: pickRandom(whos),
        actionLabel: pickRandom([...ACTIONS]),
        ...generateExtraFields(),
      });
    }
  }

  const step1Rows = rows.filter(r => r.step === 1);
  const step2Rows = rows.filter(r => r.step === 2);

  const links: Link[] = [];
  const linkTarget = Math.min(8 + Math.floor(Math.random() * 8), step1Rows.length * step2Rows.length);

  const usedPairs = new Set<string>();

  while (links.length < linkTarget) {
    const from = pickRandom(step1Rows);
    const to = pickRandom(step2Rows);
    const kind = pickRandom([...KINDS]);
    const pairKey = `${from.id}|${to.id}|${kind}`;
    if (usedPairs.has(pairKey)) {
      if (usedPairs.size >= step1Rows.length * step2Rows.length * KINDS.length) break;
      continue;
    }
    usedPairs.add(pairKey);

    links.push({
      id: createLinkId(),
      fromId: from.id,
      toId: to.id,
      kind,
    });
  }

  return { rows, links };
}
